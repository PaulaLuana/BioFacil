<p align="justify">
Gregor Mendel foi um monge agostiniano, botânico e meteorologista austríaco. 
Foi considerado o pai da genética por suas três leis, que são as leis da hereditariedade,
também chamadas de leis de Mendel.<br><br>

<b>1° Lei de Mendel: Lei da segregação dos fatores</b>
A primeira lei de Mendel diz: "Cada característica é determinada por dois fatores que se separam na formação dos gametas, onde ocorrem em dose simples",
ou seja, para cada gameta feminino ou masculino é encaminhado apenas um fator.
Com esta lei ele comprova sua hipótese de dominância e recessividade com seus experimentos.
<br>
Os primeiros experimentos de Mendel foram feitos com ervilhas, cruzando os genes de dois tipos de ervilhas diferentes.<br>
Ex:RR e Rr (dominância), rr (recessividade).

