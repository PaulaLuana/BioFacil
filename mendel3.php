<b>2° Lei de Mendel: A segregação independente de dois ou mais pares de genes</b>
<br>A segunnda lei de Mendel diz: "Os fatores para duas ou mais características segregam-se no híbrido,
distribuindo-se independentemente para os gametas, onde se combinam ao acaso".
Nessa lei, Mendel continuou a estudar as diversas características fenótipicas da ervilha. 
Mendel concluiu que a segregação independente dos fatores para duas ou mais características
era um princípio geral, constituindo uma segunda lei da herança.
<br>Ao estudar a herança simultânea de diversos pares de características. Mendel sempre observou a proporção fenotípica,
conseqüência da segregação independente ocorrida no sduplo-heterozigoto, que origina quatro tipos de gameta.
<br>Para determinar o número de tipos de gametas formados por um indivíduo, segundo a segregação independente,
basta aplicar a expressão 2n, em que n representa o número de pares de alelos no genótipo que se encontram
na condição heterozigota.
<br> Ex: A 2º lei de Mendel é um exemplo de aplicação direta da regra do E de probabilidade,
permitindo chegar aos mesmos resultados sem a construção trabalhosa de quadro de cruzamentos.
Vamos exemplificar, partindo do cruzamento entre suas plantas de ervilha duplo heterozigotas.
