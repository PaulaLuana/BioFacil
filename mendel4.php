<p align="justify">
<b>Continuação da 2° Lei de Mendel: A segregação independente de dois ou mais pares de genes</b>
<br> A 2° lei nem sempre é obedecida. A descoberta de que os genes estão situados nos cromossomos
gerou um impasse no entendimento da 2º Lei de Mendel. Mendel afirmava que os genes relacionados a
duas ou mais características sempre apresentavam segregação independente. Se essa premissa fosse verdadeira,
então haveria um cromossomo para cada gene. Se considerarmos que existe uma infinidade de genes, haveria,
então, uma quantidade assombrosa de cromossomos, dentro de uma célula, o que não é verdade. Logo,
como existem relativamente poucos cromossomos no núcleo das células e inúmeros genes, é intuitivo
concluir que, em cada cromossomo, existe uma infinidade de genes, responsáveis pelas inúmeras características
típicas de cada espécie. Dizemos que esses genes presentes em um mesmo cromossomo estão ligados ou em linkage,é quando
genes presentes em um mesmo cromossomo estão ligados,e caminham juntos para a formação dos gametas.
<br><br>
<b>3° Lei de Mendel: Lei da Distribuição Independente</b>
<br>Esta é a última lei. Mendel trabalha com os conceitos de dominância em que os seres híbridos
apresentam um caráter dominante que encobre, segundo determinadas proporções, o chamado caráter
recessivo, ou seja, os seres híbridos. Nos resultados dos cruzamento entre seres portadores de características
dominantes e recessivos, apresentam as características de dominância.
