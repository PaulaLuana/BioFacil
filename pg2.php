<p class="termos">
<b>Genótipo:</b> O termo “genótipo” (do grego genos, originar, provir, e typos, característica) refere-se à constituição genética do indivíduo, 
ou seja, aos genes que ele possui. São características do genes herdado pelos pais.
Ex: Aa, BB.<br><br>

<b>Fenótipo:</b> O termo “fenótipo” (do grego pheno, evidente, brilhante, e typos, característico) é empregado para designar as características 
apresentadas por um indivíduo, sejam elas morfológicas, fisiológicas e comportamentais.
Ex: Cor dos olhos de uma pessoa, a textura do cabelo, a cor do pelo de um animal, etc.<br><br>

<b>Homozigóticos:</b> Homozigótico corresponde à condição em que os dois genes de um par de alelos são idênticos. Assim, se considerarmos a 
característica cor de olho, por exemplo, podemos dizer que indivíduos <b>AA</b> ou <b>aa</b> são homozigóticos em relação ao par de genes que 
determina essa característica.<br><br>

<b>Heterozigóticos:</b> Heterozigótico corresponde à condição em que os dois genes de um par de alelos são diferentes entre si. Utilizando o 
mesmo exemplo, os indivíduos <b>Aa</b> são heterozigóticos. Indivíduos de genótipos <b>AA</b> e <b>Aa</b> têm fenótipo olhos escuros e indivíduos 
de genótipo <b>aa</b> têm fenótipo olhos claros.<br><br>

<b>Dominante:</b> São características que dominam sobre outras. Sempre representados por letras maiuscúlas.
Ex:AA, Aa.<br><br>

<b>Recessivo:</b> São caracteríticas com menor incidência. Sempre será homozigóticos e representado por letras minúsculas.
Ex: bb, aa.<br><br>
</p>

