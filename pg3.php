<center><p class="rh">
As pessoas que apresentam o fator <b>Rh</b> em seus glóbulos vermelhos são identificadas como <b>Rh+</b> (Rh positivas).
Aquelas que não apresentam o antígeno Rh são denominados <b>Rh-</b> (Rh negativas).
Quando se procede a uma transfusão sanguínea é necessário verificar se o receptor tem <b>Rh-</b>, pois, se assim for,
ele não poderá receber sangue do tipo <b>Rh+</b>, uma vez que, seu sistema imune produzirá anticorpos anti-Rh e vice-versa.
