<center><div id="titulo">* Interação gênica * </div><br><br></center>
A interação gênica ocorre quando dois ou mais pares de genes, com distribuição independente,
determinam conjuntamente um único caráter. Esses pares de genes interagem entre si. 
Os diferentes tipos de interação gênica são agrupados em duas características de epsasia,
que é uma modalidade da interação gênica na qual, genes de um inibem a
manifestação de genes de outro. O efeito epistético manifesta-se entre genes não alelos.
Genes epistéticos são os que impedem a atuação de outros, e hipostéticos são os genes inibidos.
<br>
Também tendo a presença da epistasia dominante, epistasia recessiva e epistasia recessiva duplicada.<br>
Epistasia dominante: Ocorre quando o alelo dominante de um par "inibe" a ação de outro par.<br>
Epistasia recessiva: Ocorre quando um par de alelos recessivos inibe a ação de alelos de outro par.<br>
Epistasia recessiva duplicada: Ocorre quando qualquer um dos alelos em homozigose recessiva é epistético sobre o gene dominante do outro par.<br>
É possível explicar a interação gênica epistética através de um exemplo clássico na genêtica: a
